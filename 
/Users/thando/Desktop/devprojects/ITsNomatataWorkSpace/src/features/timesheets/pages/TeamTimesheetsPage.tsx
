function formatDuration(seconds: number) {
  const total = Math.max(0, Number(seconds || 0));
  const hours = Math.floor(total / 3600);
  const minutes = Math.floor((total % 3600) / 60);
  if (hours > 0) return `${hours}h ${minutes}m`;
  return `${minutes}m`;
}

function formatHours(hours: number) {
  if (!hours) return "-";
  return `${hours.toFixed(1)}h`;
}

function getDateKey(date: Date) {
  return date.toISOString().slice(0, 10);
}

function startOfWeek(date: Date, weekStart = 1) {
  const copy = new Date(date);
  const day = copy.getDay();
  const distance = (day + 7 - weekStart) % 7;
  copy.setDate(copy.getDate() - distance);
  copy.setHours(0, 0, 0, 0);
  return copy;
}

function buildTwoWeekDays() {
  const today = new Date();
  const currentWeekStart = startOfWeek(today, 1);
  const previousWeekStart = new Date(currentWeekStart);
  previousWeekStart.setDate(previousWeekStart.getDate() - 7);
  const weeks = [previousWeekStart, currentWeekStart];

  return weeks.flatMap((start, weekIndex) =>
    Array.from({ length: 7 }, (_, dayIndex) => {
      const date = new Date(start);
      date.setDate(start.getDate() + dayIndex);
      return {
        date,
        key: getDateKey(date),
        label: date
          .toLocaleDateString(undefined, { weekday: "short" })
          .slice(0, 2),
        weekLabel: weekIndex === 0 ? "Last week" : "This week",
      };
    }),
  );
}

